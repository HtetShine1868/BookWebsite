document.getElementById('searchBar').addEventListener('keyup', (e) => {
    const searchData = e.target.value.toLowerCase();
    const filteredData = product.filter((item) => {
        return item.title.toLowerCase().includes(searchData);
    });
    displayItem(filteredData, searchData);
});

const displayItem = (items, searchData) => {
    const rootElement = document.getElementById('root');

    if (searchData === '') {
        // If search input is empty, hide the boxes
        rootElement.innerHTML = '';
        return;
    }

    rootElement.innerHTML = items.map((item) => {
        const { link, title } = item;
        return `<div class='box'><a class="link-is-link" href="${link}">${title}</a></div>`;
    }).join('');
};


